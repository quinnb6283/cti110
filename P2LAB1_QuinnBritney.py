#Create a program will calculate the diameter, circumference, and area of a circle. 




rad = float(input(f"What is the radius of the circle? "))

diam = rad*2

pi=3.14159


print (f'The diameter of the circle is {diam:.1f}')

circum = 2*pi*rad

print (f'The circumference of the circle is {circum:.2f}')

area = pi*rad**2

print (f'The area of the circle is {area:.3f}')










